public class IntegerOperations {
    public static void main(String[] args) {
        int age = 25;

        System.out.println(age + 10); // 35
        System.out.println(age - 10); // 15
        System.out.println(age * 10); // 250
        System.out.println(age / 10); // 2
        System.out.println(age % 10); // 5
        System.out.println(age);
    }

}
